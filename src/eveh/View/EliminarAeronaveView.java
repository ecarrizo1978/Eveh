    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        
        if(txtMatricula.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Debe Ingresar una matricula","Error",JOptionPane.ERROR_MESSAGE);
        }
        else{ 
            try {
                
                
                
                a = c.ListarAeronaveByMatricula(txtMatricula.getText());
                
                if(a.getTipoAeronave()<=0)
                {
                    btnEliminar.setEnabled(false);
                    JOptionPane.showMessageDialog(null, "No Existe Aeronave","Error",JOptionPane.ERROR_MESSAGE);
                }
                else
                {
                    btnEliminar.setEnabled(true);
                    lblMatricula.setText(a.getMatricula());
                    lblFabricacion.setText(Integer.toString(a.getAnioFabricacion()));
                    lblHv.setText(Integer.toString(a.getHorasVuelo()));
                    lblMantencion.setText(a.getFechaMantencion());
                    if(a.getDisponible()==0)
                    { lblDisponible.setText("No"); }
                    else
                    { lblDisponible.setText("Si"); }
                    if(a.getVigencia()==0)
                    { lblVigencia.setText("No"); }
                    else
                    { lblVigencia.setText("Si"); }
                    ta = c.ListarTipoAeronaveById(a.getTipoAeronave());
                    lblTipoAeronave.setText(ta.getDetalle());
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(EliminarAeronaveView.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        try {
            
            c.EliminarAeronave(a.getMatricula());
            lblMatricula.setText("");
            lblFabricacion.setText("");
            lblHv.setText("");
            lblMantencion.setText("");
            lblDisponible.setText("");
            lblVigencia.setText("");
            lblTipoAeronave.setText("");
            txtMatricula.setText(null);
            btnEliminar.setEnabled(false);
            
        } catch (SQLException ex) {
            Logger.getLogger(EliminarAeronaveView.class.getName()).log(Level.SEVERE, null, ex);
        }
      
        
        
    }//GEN-LAST:event_btnEliminarActionPerformed


