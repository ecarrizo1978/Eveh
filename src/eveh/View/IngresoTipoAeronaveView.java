    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        if(txtDetalle.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Debe Ingresar Tipo de Aeronave");
        }
        else if(cbxVigencia.getSelectedItem().toString() == "Seleccione Vigencia")
        {
            JOptionPane.showMessageDialog(null, "Debe Seleccionar Vigencia");
        }
        else 
        {
            try {
                ta.setDetalle(txtDetalle.getText());
                ta.setVigencia(Integer.parseInt(map.get(cbxVigencia.getSelectedItem().toString()).toString()));
                if(c.IngresarTipoAeronave(ta))
                {
                    txtDetalle.setText("");
                }
            } catch (SQLException ex) {
                Logger.getLogger(IngresoTipoAeronaveView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_btnIngresarActionPerformed


